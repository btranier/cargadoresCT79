INSERT INTO meters (gateway_id, unit_id, slot_code, description, status, multiplier)
SELECT
  g.id,
  n.unit_id,
  printf('NA-%s-U%d', g.host, n.unit_id) AS slot_code,
  'Auto-created from readings_20260201 2.csv' AS description,
  'Inactivo' AS status,
  1.0 AS multiplier
FROM non_active n
JOIN gateways g
  ON g.host = n.gateway_host AND g.port = n.gateway_port
WHERE NOT EXISTS (
  SELECT 1 FROM meters m WHERE m.gateway_id = g.id AND m.unit_id = n.unit_id
);
